public class SBlocks 
{
	static
	{
		System.out.println("Inside STATIC BLOCK1");		
	}
	
	static
	{
		
		System.out.println("Inside STATIC BLOCK2");		
	}

	public static void main(String[] args) 
	{
		System.out.println("Program Starts");
		System.out.println();
		System.out.println("Program Ends");

	}

}
